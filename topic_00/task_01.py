g = "Hello Ilya"
g1 = "You're welcome"
g2 = "JUST A WORD"

print(g.strip())        # видаляє пробіли з початку та кінця рядка
print(g2.capitalize())  # робить першу літеру великою, а решту малими
print(g1.title())       # робить першу літеру КОЖНОГО слова великою
print(g.upper())        # переводить увесь текст у ВЕЛИКІ літери
print(g2.lower())       # переводить увесь текст у малі літери
print(g.swapcase())     # міняє регістр: великі → малі, малі → великі